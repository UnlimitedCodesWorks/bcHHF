package com.c6.city.model;

public class Activity {
	
	private String acName;
	
	private String acContent;
	
	private String startTime;
	
	private String admin;
	
	private String acIcon;
	
	private String acImg;
	
	private String acImgTitle;

	public String getAcName() {
		return acName;
	}

	public void setAcName(String acName) {
		this.acName = acName;
	}

	public String getAcContent() {
		return acContent;
	}

	public void setAcContent(String acContent) {
		this.acContent = acContent;
	}

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getAdmin() {
		return admin;
	}

	public void setAdmin(String admin) {
		this.admin = admin;
	}

	public String getAcIcon() {
		return acIcon;
	}

	public void setAcIcon(String acIcon) {
		this.acIcon = acIcon;
	}

	public String getAcImg() {
		return acImg;
	}

	public void setAcImg(String acImg) {
		this.acImg = acImg;
	}

	public String getAcImgTitle() {
		return acImgTitle;
	}

	public void setAcImgTitle(String acImgTitle) {
		this.acImgTitle = acImgTitle;
	}
}
