package com.c6.city.model;

public class News {
	
	private String newsName;
	
	private String newsContent;
	
	private String startTime;
	
	private String admin;
	
	private String newsIcon;
	
	private String newsRank;
	
	private String newsImage;
	
	private String newsImgTitle;

	public String getNewsName() {
		return newsName;
	}

	public void setNewsName(String newsName) {
		this.newsName = newsName;
	}

	public String getNewsContent() {
		return newsContent;
	}

	public void setNewsContent(String newsContent) {
		this.newsContent = newsContent;
	}

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getAdmin() {
		return admin;
	}

	public void setAdmin(String admin) {
		this.admin = admin;
	}

	public String getNewsIcon() {
		return newsIcon;
	}

	public void setNewsIcon(String newsIcon) {
		this.newsIcon = newsIcon;
	}

	public String getNewsRank() {
		return newsRank;
	}

	public void setNewsRank(String newsRank) {
		this.newsRank = newsRank;
	}

	public String getNewsImage() {
		return newsImage;
	}

	public void setNewsImage(String newsImage) {
		this.newsImage = newsImage;
	}

	public String getNewsImgTitle() {
		return newsImgTitle;
	}

	public void setNewsImgTitle(String newsImgTitle) {
		this.newsImgTitle = newsImgTitle;
	}
	
}
