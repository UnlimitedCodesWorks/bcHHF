package com.c6.city.model;

public class TripItem {
	
	private String tripTitle;
	
	private String tripContent;
	
	private String tripIcon;
	
	private String tripType;
	
	private String tripPrice;
	
	private int tripRank;
	
	private String tripStart;
	
	private String tripEnd;
	
	private int tripDay;
	
	private String specialContent;

	public String getTripTitle() {
		return tripTitle;
	}

	public void setTripTitle(String tripTitle) {
		this.tripTitle = tripTitle;
	}

	public String getTripContent() {
		return tripContent;
	}

	public void setTripContent(String tripContent) {
		this.tripContent = tripContent;
	}

	public String getTripIcon() {
		return tripIcon;
	}

	public void setTripIcon(String tripIcon) {
		this.tripIcon = tripIcon;
	}

	public String getTripType() {
		return tripType;
	}

	public void setTripType(String tripType) {
		this.tripType = tripType;
	}

	public String getTripPrice() {
		return tripPrice;
	}

	public void setTripPrice(String tripPrice) {
		this.tripPrice = tripPrice;
	}

	public int getTripRank() {
		return tripRank;
	}

	public void setTripRank(int tripRank) {
		this.tripRank = tripRank;
	}

	public String getTripStart() {
		return tripStart;
	}

	public void setTripStart(String tripStart) {
		this.tripStart = tripStart;
	}

	public String getTripEnd() {
		return tripEnd;
	}

	public void setTripEnd(String tripEnd) {
		this.tripEnd = tripEnd;
	}

	public int getTripDay() {
		return tripDay;
	}

	public void setTripDay(int tripDay) {
		this.tripDay = tripDay;
	}

	public String getSpecialContent() {
		return specialContent;
	}

	public void setSpecialContent(String specialContent) {
		this.specialContent = specialContent;
	}
	
	
}
