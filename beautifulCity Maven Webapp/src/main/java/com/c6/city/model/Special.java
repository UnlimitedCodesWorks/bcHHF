package com.c6.city.model;

public class Special {
	
	private String specialType;
	
	private String specialImg;
	
	private String speImgTitle;
	
	private String speImgContent;

	public String getSpecialType() {
		return specialType;
	}

	public void setSpecialType(String specialType) {
		this.specialType = specialType;
	}

	public String getSpecialImg() {
		return specialImg;
	}

	public void setSpecialImg(String specialImg) {
		this.specialImg = specialImg;
	}

	public String getSpeImgTitle() {
		return speImgTitle;
	}

	public void setSpeImgTitle(String speImgTitle) {
		this.speImgTitle = speImgTitle;
	}

	public String getSpeImgContent() {
		return speImgContent;
	}

	public void setSpeImgContent(String speImgContent) {
		this.speImgContent = speImgContent;
	}
}
