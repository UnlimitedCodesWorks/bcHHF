package com.c6.city.model;

public class Village {
	private String villPlace;
	
	private String villContent;
	
	private String villImg;

	public String getVillPlace() {
		return villPlace;
	}

	public void setVillPlace(String villPlace) {
		this.villPlace = villPlace;
	}

	public String getVillContent() {
		return villContent;
	}

	public void setVillContent(String villContent) {
		this.villContent = villContent;
	}

	public String getVillImg() {
		return villImg;
	}

	public void setVillImg(String villImg) {
		this.villImg = villImg;
	}
}
